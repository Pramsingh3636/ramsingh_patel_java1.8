package com.telusko.java;

public class SwitchDemo {

	public static void main(String[] args) {
		//Switch Case
		String d = "Ram";
		switch(d)
		{
		case "abc":
			System.out.println("one");
			break;
		case "pqr":
			System.out.println("two");
			break;
		default:
			System.out.println("no match");
		}
		
	}

}
